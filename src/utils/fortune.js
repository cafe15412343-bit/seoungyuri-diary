export function getTodayFortune() {
  const today = new Date()
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()

  // 두 사람 생년월일로 시드 보정
  const coupleSeed = seed + BIRTH.seungbin.month * 100 + BIRTH.seungbin.day + BIRTH.gyuri.month * 100 + BIRTH.gyuri.day

  const hash = (s, offset) => {
    let h = s + offset * 7919
    h = ((h >> 16) ^ h) * 0x45d9f3b
    h = ((h >> 16) ^ h) * 0x45d9f3b
    h = (h >> 16) ^ h
    return Math.abs(h)
  }

  const overall = fortunePool.overall[hash(coupleSeed, 1) % fortunePool.overall.length]
  const date = fortunePool.date[hash(coupleSeed, 2) % fortunePool.date.length]
  const love = fortunePool.love[hash(coupleSeed, 3) % fortunePool.love.length]
  const advice = fortunePool.advice[hash(coupleSeed, 4) % fortunePool.advice.length]

  return {
    overall: overall.msg,
    overallScore: overall.score,
    date: date.msg,
    dateTip: date.tip,
    love: love.msg,
    loveScore: love.score,
    advice: advice,
  }
}
