export function getTodayFortune() {
  const today = new Date()
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()
  
  const hash = (s, offset) => {
    let h = s + offset * 7919
    h = ((h >> 16) ^ h) * 0x45d9f3b
    h = ((h >> 16) ^ h) * 0x45d9f3b
    h = (h >> 16) ^ h
    return Math.abs(h)
  }

  return {
    overall: fortunes.overall[hash(seed, 1) % fortunes.overall.length],
    date: fortunes.date[hash(seed, 2) % fortunes.date.length],
    love: fortunes.love[hash(seed, 3) % fortunes.love.length],
  }
}
